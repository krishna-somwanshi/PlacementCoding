void right(int n)
{
    int k1 = (n-1)/2;
    int k2 = 3 * n / 2 - 1 ;
    for(int i = 0 ; i < n ; i++)
    {
        for( int j = 0 ; j < n ; j++)
        {
            if( j - i == k1 || i + j == k2 || i == k1)
                cout<<"*";
            else
                cout<<" ";
        }
        cout<<endl;
    }    
}

void left(int n)
{
    cout<<endl;
    cout<<endl;
    
int k1 = (n - 1) / 2;  
    for (int i = k1; i > 0; i--)          
        cout << string(i,' ') << "*" << endl; 
    for (int i = 0; i < n; i++) 
        cout << "*"; 
    cout << endl; 
    for (int i = 1; i <= k1; i++) 
        cout << string(i, ' ') << "*" << endl; 
    cout << endl; 
    }



